package supermark;

public class Par {

	private int primer;
	private double segundo;
	public Par(int primer, double segundo) {
		super();
		this.primer = primer;
		this.segundo = segundo;
	}
	public int getPrimer() {
		return primer;
	}
	public void setPrimer(int primer) {
		this.primer = primer;
	}
	public double getSegundo() {
		return segundo;
	}
	public void setSegundo(double segundo) {
		this.segundo = segundo;
	}
	
	
}
